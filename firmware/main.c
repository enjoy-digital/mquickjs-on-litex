/* Live JavaScript loader                                               */
/* -------------------------------------------------------------------- */

#if LITEX_MQJS_LIVE_DEMO

#if LITEX_MQJS_LIVE_HTTP

#if defined(CSR_ETHMAC_BASE)

static void live_loop(JSContext **ctxp)
{
    live_http_loop(ctxp);
}

#else

static void live_loop(JSContext **ctxp)
{
    (void)ctxp;
    puts("[live] Ethernet support is not present in this SoC");
}

#endif

#elif LITEX_MQJS_LIVE_UDP

#ifndef LITEX_MQJS_LIVE_PORT
#define LITEX_MQJS_LIVE_PORT 12345
#endif

#if defined(CSR_ETHMAC_BASE)

#ifdef MACADDR1
static const uint8_t live_mac[6] = {MACADDR1, MACADDR2, MACADDR3,
                                    MACADDR4, MACADDR5, MACADDR6};
#else
static const uint8_t live_mac[6] = {0x10, 0xe2, 0xd5, 0x00, 0x00, 0x00};
#endif

#ifdef LOCALIP1
static const unsigned int live_ip[4] = {LOCALIP1, LOCALIP2, LOCALIP3, LOCALIP4};
#else
static const unsigned int live_ip[4] = {192, 168, 1, 50};
#endif

static char live_script[8192];
static volatile uint32_t live_script_len;
static volatile uint8_t live_pending;
static uint32_t live_src_ip;
static uint16_t live_src_port;

static void live_udp_rx(uint32_t src_ip, uint16_t src_port, uint16_t dst_port,
                        void *data, uint32_t length)
{
    if (dst_port != LITEX_MQJS_LIVE_PORT)
        return;
    if (length >= sizeof(live_script))
        length = sizeof(live_script) - 1;

    memcpy(live_script, data, length);
    live_script[length] = 0;
    live_script_len = length;
    live_src_ip = src_ip;
    live_src_port = src_port;
    live_pending = 1;
}

static void live_send_reply(uint32_t dst_ip, uint16_t dst_port, const char *text)
{
    size_t len = strlen(text);
    char *tx;

    if (len > UDP_BUFSIZE)
        len = UDP_BUFSIZE;
    if (!udp_arp_resolve(dst_ip))
        return;
    tx = udp_get_tx_buffer();
    memcpy(tx, text, len);
    udp_send(LITEX_MQJS_LIVE_PORT, dst_port, len);
}

static void live_loop(JSContext **ctxp)
{
    puts("[live] starting Ethernet JavaScript receiver");
    printf("[live] local IP: %u.%u.%u.%u\n",
           live_ip[0], live_ip[1], live_ip[2], live_ip[3]);
    printf("[live] UDP port: %u\n", LITEX_MQJS_LIVE_PORT);
    puts("[live] run: ./tools/live_bridge.py --board 192.168.1.50");

    eth_init();
    udp_start(live_mac, IPTOINT(live_ip[0], live_ip[1], live_ip[2], live_ip[3]));
    udp_set_callback(live_udp_rx);

    for (;;) {
        udp_service();
        if (!live_pending)
            continue;

        live_pending = 0;
        printf("[live] received %u bytes from %u.%u.%u.%u:%u\n",
               (unsigned)live_script_len,
               (unsigned)((live_src_ip >> 24) & 0xff),
               (unsigned)((live_src_ip >> 16) & 0xff),
               (unsigned)((live_src_ip >>  8) & 0xff),
               (unsigned)((live_src_ip >>  0) & 0xff),
               live_src_port);

        JS_FreeContext(*ctxp);
        *ctxp = new_mqjs_context();
        if (*ctxp == NULL) {
            puts("[live] JS_NewContext failed");
            live_send_reply(live_src_ip, live_src_port, "ERR JS_NewContext failed\n");
            continue;
        }

        if (run_source(*ctxp, live_script, live_script_len,
                       "live_udp.js", JS_EVAL_REPL) == 0) {
            puts("[live] script ok");
            live_send_reply(live_src_ip, live_src_port, "OK\n");
        } else {
            puts("[live] script failed");
            live_send_reply(live_src_ip, live_src_port, "ERR script failed\n");
        }
    }
}

#else

static void live_loop(JSContext **ctxp)
{
    (void)ctxp;
    puts("[live] Ethernet support is not present in this SoC");
}

#endif

#endif /* LITEX_MQJS_LIVE_HTTP/LITEX_MQJS_LIVE_UDP */

#endif /* LITEX_MQJS_LIVE_DEMO */

/* -------------------------------------------------------------------- */
/* Main                                                                 */
/* -------------------------------------------------------------------- */

int main(void)
{
#ifdef CONFIG_CPU_HAS_INTERRUPT
    irq_setmask(0);
    irq_setie(1);
#endif
    uart_init();

    puts("\n"
         "   __  _______       _     __      ______              __   _ __      _  __\n"
         "  /  |/  / __ \\__ __(_)___/ /____ / / __/ ___  ___    / /  (_) /____ | |/_/\n"
         " / /|_/ / /_/ / // / / __/  '_/ // /\\ \\  / _ \\/ _ \\  / /__/ / __/ -_)>  <\n"
         "/_/  /_/\\___\\_\\_,_/_/\\__/_/\\_\\\\___/___/  \\___/_//_/ /____/_/\\__/\\__/_/|_|\n");
    puts("--========= mquickjs on LiteX =========--");
    printf("mquickjs heap:   %u bytes\n", (unsigned)sizeof(mqjs_heap));
    printf("CPU:             %s @ %u Hz\n",
           CONFIG_CPU_HUMAN_NAME, (unsigned)CONFIG_CLOCK_FREQUENCY);

    JSContext *ctx = new_mqjs_context();
    if (!ctx) {
        puts(LITEX_MQJS_FAIL_MARKER ": JS_NewContext failed");
        while (1) { }
    }

#if defined(MQJS_SCRIPT_EMBEDDED)
    puts("running embedded script...");
    int rc = run_source(ctx, (const char *)user_script, user_script_len,
                        "user_script.js", JS_EVAL_REPL);
#if LITEX_MQJS_DUMP_MEMORY
    /* Run GC before dumping so the numbers reflect live state rather
     * than the watermark at program exit. */
    JS_GC(ctx);
    fputs("mqjs memory: ", stdout);
    JS_DumpMemory(ctx, 0);
#endif
    puts(rc == 0 ? LITEX_MQJS_DONE_MARKER : LITEX_MQJS_FAIL_MARKER);
#if LITEX_MQJS_LIVE_DEMO
    if (rc == 0)
        live_loop(&ctx);
#endif
#else
    repl(ctx);
    puts(LITEX_MQJS_DONE_MARKER);
#endif

    /* Halt — the simulator watches for the DONE/FAIL marker above and
     * shuts down the run. */
    for (;;) { }
    return 0;
}
