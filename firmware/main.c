// Entry point for the LiteX-mquickjs firmware.
//
// Two execution modes are selected at compile time:
//   - MQJS_SCRIPT_EMBEDDED: `user_script.h` provides `user_script[]` and
//     `user_script_len`. The firmware evaluates it once at boot and prints
//     a completion marker that the test harness scrapes from the UART.
//   - otherwise: a minimal line-based REPL over UART, terminated by the
//     literal string `.exit` on its own line.

